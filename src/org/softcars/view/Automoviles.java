    private void cargarTabla(){
        DefaultTableModel modeloTable = (DefaultTableModel) tblVenta.getModel();
        modeloTable.setRowCount(0);
        PreparedStatement pstmt;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        try{
         ConexionNube conNube = new ConexionNube();
         Connection conn = conNube.open();
         pstmt = conn.prepareStatement("SELECT * FROM ventaAutos;");
         rs = pstmt.executeQuery();
         rsmd = (ResultSetMetaData) rs.getMetaData();
         columnas = rsmd.getColumnCount();
         while(rs.next()){
             Object[] fila = new Object[columnas];
             for(int indice = 0;indice < columnas;indice ++){
                 fila[indice] = rs.getObject(indice + 1);
             }
             modeloTable.addRow(fila);
         }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex.toString());
        }  
    } 
    
