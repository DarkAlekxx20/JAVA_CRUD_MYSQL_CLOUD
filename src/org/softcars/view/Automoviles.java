         pstmt.setString(1,marca);
         pstmt.setInt(2,modelo);
         pstmt.setDouble(3,precioCompra);
         pstmt.setDouble(4,gastos);
         pstmt.setDouble(5,totalVenta);
         pstmt.setDouble(6,venta);
         pstmt.setString(7,mes);
         pstmt.setDouble(8,ganancia);
         pstmt.setInt(9,idVenta);
